-module(wsclient).

-export([
	start/0,
	stop/0	
]).

%%--------------------------------------------------------------------
%%
%%--------------------------------------------------------------------
start() -> 	

	start_applications(
		file:consult(
			"relx.config"
		),
		start
	).
%%--------------------------------------------------------------------
%%
%%--------------------------------------------------------------------
start_applications({ok,[Config|_]},Fun) ->

	lists:map(fun(Name) ->

		application:Fun(Name)

	end, erlang:element(3,Config));

start_applications(_,_) -> [].
%%--------------------------------------------------------------------
%%
%%--------------------------------------------------------------------
stop() -> application:stop(polbot).