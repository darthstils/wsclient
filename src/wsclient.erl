-module(wsclient).

-export([
	start/0,
	stop/0
]).

%%--------------------------------------------------------------------
%%
%%--------------------------------------------------------------------
start() -> 	

	start_applications(
		file:consult(
			"relx.config"
		)
	).
%%--------------------------------------------------------------------
%%
%%--------------------------------------------------------------------
start_applications({ok,[Config|_]}) ->

	lists:map(fun(Name) ->

		application:start(Name)

	end, erlang:element(3,Config));

start_applications(_) -> [].
%%--------------------------------------------------------------------
%%
%%--------------------------------------------------------------------
stop() -> application:stop(polbot).